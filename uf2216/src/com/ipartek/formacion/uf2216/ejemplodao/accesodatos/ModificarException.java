package com.ipartek.formacion.uf2216.ejemplodao.accesodatos;

public class ModificarException extends AccesoDatosException {

	private static final long serialVersionUID = -8812453857632734653L;

	public ModificarException() {
	}

	public ModificarException(String message) {
		super(message);
	}

	public ModificarException(Throwable cause) {
		super(cause);
	}

	public ModificarException(String message, Throwable cause) {
		super(message, cause);
	}

	public ModificarException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
