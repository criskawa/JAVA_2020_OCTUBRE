package com.ipartek.formacion.uf2216.ejemplodao.accesodatos;

public class InsertarException extends AccesoDatosException {

	private static final long serialVersionUID = -1369246191402475380L;

	public InsertarException() {
	}

	public InsertarException(String message) {
		super(message);
	}

	public InsertarException(Throwable cause) {
		super(cause);
	}

	public InsertarException(String message, Throwable cause) {
		super(message, cause);
	}

	public InsertarException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

}
